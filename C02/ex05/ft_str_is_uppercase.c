int		ft_str_is_uppercase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c >= 'A' && c <= 'Z')
		{
			i++;
			continue;
		}
		else
			return (0);
	}
	return (1);
}
